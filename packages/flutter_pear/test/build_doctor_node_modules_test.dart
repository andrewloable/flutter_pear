import 'dart:convert';
import 'dart:io';

import 'package:flutter_test/flutter_test.dart';

import '../tool/build_doctor_node_modules.dart';

/// Writes a fake npm package at `<root>/<name>` with a `package.json`
/// declaring [deps], a plain `index.js`, and (if [withPrebuilds]) a
/// `.node`+`.bare` pair under one desktop platform (`darwin-arm64`) and one
/// non-desktop platform (`android-arm64`) -- enough to exercise every branch
/// of [copyTrimmedPackage] without a real npm install.
void _writeFakePackage(
  Directory root,
  String name, {
  List<String> deps = const [],
  bool withPrebuilds = false,
}) {
  final dir = Directory('${root.path}/$name')..createSync(recursive: true);
  File('${dir.path}/package.json').writeAsStringSync(jsonEncode({
    'name': name,
    'version': '1.0.0',
    if (deps.isNotEmpty) 'dependencies': {for (final d in deps) d: '^1.0.0'},
  }));
  File('${dir.path}/index.js').writeAsStringSync('module.exports = {}\n');
  if (withPrebuilds) {
    for (final platform in ['darwin-arm64', 'android-arm64']) {
      final prebuildDir = Directory('${dir.path}/prebuilds/$platform')
        ..createSync(recursive: true);
      File('${prebuildDir.path}/$name.node').writeAsBytesSync([1, 2, 3]);
      File('${prebuildDir.path}/$name.bare').writeAsBytesSync([4, 5, 6]);
    }
  }
}

void main() {
  late Directory tmp;

  setUp(() {
    tmp = Directory.systemTemp.createTempSync('fp_doctor_node_modules');
  });

  tearDown(() => tmp.deleteSync(recursive: true));

  test(
      'resolveDependencyClosure walks package.json dependencies transitively, '
      'root package included', () {
    final nm = Directory('${tmp.path}/node_modules')..createSync();
    _writeFakePackage(nm, 'root-pkg', deps: ['dep-a', 'dep-b']);
    _writeFakePackage(nm, 'dep-a', deps: ['dep-c']);
    _writeFakePackage(nm, 'dep-b');
    _writeFakePackage(nm, 'dep-c');

    final closure = resolveDependencyClosure(nm, 'root-pkg');

    expect(closure, {'root-pkg', 'dep-a', 'dep-b', 'dep-c'});
  });

  test(
      'resolveDependencyClosure skips (does not throw on) a dependency '
      'missing from node_modules -- a stale/pruned install, not a reason to '
      'crash the whole regeneration', () {
    final nm = Directory('${tmp.path}/node_modules')..createSync();
    _writeFakePackage(nm, 'root-pkg', deps: ['dep-a', 'ghost-pkg']);
    _writeFakePackage(nm, 'dep-a');

    final closure = resolveDependencyClosure(nm, 'root-pkg');

    expect(closure, {'root-pkg', 'dep-a', 'ghost-pkg'});
    // ghost-pkg was walked (added to the seen/returned set so it's never
    // revisited) but has no package.json, so copyTrimmedPackage would find
    // nothing to copy for it -- covered by the end-to-end test below.
  });

  test(
      'buildDoctorNodeModules keeps only desktop-platform .node prebuilds, '
      'drops .bare files and non-desktop platforms entirely '
      '(flutter_pear-ewf)', () {
    final pearEndNm =
        Directory('${tmp.path}/pear-end/node_modules')..createSync(recursive: true);
    _writeFakePackage(pearEndNm, 'hyperswarm', deps: ['native-dep']);
    _writeFakePackage(pearEndNm, 'native-dep', withPrebuilds: true);

    final result =
        buildDoctorNodeModules(tmp.path, rootPackage: 'hyperswarm');

    expect(result.packageCount, 2);

    final target = Directory('${tmp.path}/tool/node_modules');
    expect(File('${target.path}/hyperswarm/index.js').existsSync(), isTrue);
    expect(
        File('${target.path}/native-dep/index.js').existsSync(), isTrue);
    expect(
        File('${target.path}/native-dep/prebuilds/darwin-arm64/native-dep.node')
            .existsSync(),
        isTrue,
        reason: 'the desktop .node prebuild must be kept');
    expect(
        File('${target.path}/native-dep/prebuilds/darwin-arm64/native-dep.bare')
            .existsSync(),
        isFalse,
        reason: '.bare files are Bare-addon-ABI binaries Node cannot load '
            '(confirmed by testing dlopen) -- must be dropped');
    expect(
        Directory('${target.path}/native-dep/prebuilds/android-arm64')
            .existsSync(),
        isFalse,
        reason: 'non-desktop platforms are never needed by doctor and must '
            'be dropped entirely');
  });

  test(
      'buildDoctorNodeModules wipes a stale tool/node_modules/ before '
      'regenerating -- never a merge with a previous run', () {
    final pearEndNm =
        Directory('${tmp.path}/pear-end/node_modules')..createSync(recursive: true);
    _writeFakePackage(pearEndNm, 'hyperswarm');

    final staleFile =
        File('${tmp.path}/tool/node_modules/leftover-from-an-old-dep/junk.js')
          ..createSync(recursive: true);
    expect(staleFile.existsSync(), isTrue);

    buildDoctorNodeModules(tmp.path, rootPackage: 'hyperswarm');

    expect(staleFile.existsSync(), isFalse);
    expect(
        File('${tmp.path}/tool/node_modules/hyperswarm/index.js').existsSync(),
        isTrue);
  });
}
