// flutter_pear-ewf: regenerates tool/node_modules/ -- a trimmed, COMMITTED
// npm dependency tree (hyperswarm + its full transitive closure) that
// doctor-checks.js/doctor-loopback-peer.js require() directly, so
// `dart run flutter_pear:doctor`'s DHT/NAT/loopback checks actually run for
// every pub.dev consumer, not just inside this repo.
//
// Why this exists: pear-end/node_modules (what those two scripts used to
// createRequire() against) is gitignored and never published -- the
// published package ships pear-end/package.json but not its node_modules,
// so `require('hyperswarm')` always threw MODULE_NOT_FOUND for a hosted
// install. The already-committed assets/desktop/<host>/node_modules/*/
// prebuilds/*.bare files (built for the `bare` runtime's own addon
// resolver, E-D3) are NOT usable here either -- confirmed by testing,
// Node's own dlopen rejects a `.bare` binary outright ("Module did not
// self-register"): same N-API surface, different addon-registration
// convention, not interchangeable.
//
// So this tree is separate, dedicated, and trimmed: JS + package.json for
// every package in hyperswarm's dependency closure, plus ONLY each
// package's desktop-platform `.node` prebuilds (darwin-arm64, darwin-x64,
// linux-x64, win32-x64 -- the same four hosts flutter_pear already builds
// desktop support for), dropping `.bare` files and every mobile/simulator
// prebuild, which doctor never needs and would otherwise roughly
// quintuple this tree's size (measured: ~4.9MB trimmed vs ~23MB full).
//
// Usage: `dart run tool/build_doctor_node_modules.dart`, run from the
// flutter_pear package directory after `npm install` in pear-end/ (this
// reads FROM pear-end/node_modules, so that must exist and be current).
// Commit the regenerated tool/node_modules/ whenever hyperswarm's
// dependency tree changes -- same "committed, regenerated by hand"
// convention as assets/pear-end.bundle and the Android/iOS addon dirs
// (flutter_pear-k2y). Every package copied here is already covered by
// `pear-end/node_modules`'s own license scan (bin/pack.dart's
// collectThirdPartyLicenses) -- this is a second, trimmed copy of
// already-vetted packages, not a new dependency.

import 'dart:convert';
import 'dart:io';

const desktopPlatforms = [
  'darwin-arm64',
  'darwin-x64',
  'linux-x64',
  'win32-x64',
];
const doctorRootPackage = 'hyperswarm';

void main() {
  final packageRoot = Directory.current.path;
  if (!Directory('$packageRoot/pear-end/node_modules').existsSync()) {
    stderr.writeln(
        'pear-end/node_modules missing -- run `npm install` in pear-end/ first.');
    exitCode = 1;
    return;
  }

  final written = buildDoctorNodeModules(packageRoot);
  stdout.writeln(
      'Wrote ${(written.totalBytes / (1024 * 1024)).toStringAsFixed(1)}MB '
      '(${written.packageCount} packages) to $packageRoot/tool/node_modules '
      '(${desktopPlatforms.join(', ')} .node prebuilds only).');
}

/// [packageCount] resolved into the dependency closure, [totalBytes]
/// written to the regenerated tree.
typedef BuildResult = ({int packageCount, int totalBytes});

/// Regenerates `tool/node_modules/` (under [pkgRoot]) from `pear-end/
/// node_modules/` (also under [pkgRoot]) -- see this file's header for what
/// and why. Wipes any existing `tool/node_modules/` first, so the result is
/// always exactly what the CURRENT `pear-end/node_modules` resolves to,
/// never a stale merge with a previous run.
BuildResult buildDoctorNodeModules(String pkgRoot,
    {String rootPackage = doctorRootPackage}) {
  final pearEndNodeModules = Directory('$pkgRoot/pear-end/node_modules');
  final closure = resolveDependencyClosure(pearEndNodeModules, rootPackage);

  final target = Directory('$pkgRoot/tool/node_modules');
  if (target.existsSync()) target.deleteSync(recursive: true);
  target.createSync(recursive: true);

  var totalBytes = 0;
  for (final name in closure) {
    final source = Directory('${pearEndNodeModules.path}/$name');
    final dest = Directory('${target.path}/$name');
    totalBytes += copyTrimmedPackage(source, dest);
  }
  return (packageCount: closure.length, totalBytes: totalBytes);
}

/// Walks [root]'s real package.json `dependencies` fields (not a lockfile)
/// starting at [rootPackage], returning every package name reached
/// ([rootPackage] included).
Set<String> resolveDependencyClosure(Directory root, String rootPackage) {
  final seen = <String>{};
  final stack = [rootPackage];
  while (stack.isNotEmpty) {
    final name = stack.removeLast();
    if (!seen.add(name)) continue;
    final pkgJson = File('${root.path}/$name/package.json');
    if (!pkgJson.existsSync()) {
      stderr.writeln('warning: ${pkgJson.path} not found -- skipping (is '
          'pear-end/node_modules stale? try `npm install` in pear-end/)');
      continue;
    }
    final decoded =
        jsonDecode(pkgJson.readAsStringSync()) as Map<String, Object?>;
    final deps =
        (decoded['dependencies'] as Map<String, Object?>?) ?? const {};
    stack.addAll(deps.keys);
  }
  return seen;
}

/// Copies [source] to [dest]: every file verbatim EXCEPT `prebuilds/`,
/// where only [desktopPlatforms] subdirectories' `.node` files are kept
/// (`.bare` files and every other platform/arch are dropped). Returns the
/// number of bytes written.
int copyTrimmedPackage(Directory source, Directory dest) {
  var bytes = 0;
  for (final entity in source.listSync(recursive: true)) {
    final relative = entity.path.substring(source.path.length + 1);
    final parts = relative.split(Platform.pathSeparator);
    if (parts.first == 'prebuilds') {
      final isKeptDesktopNodeFile = parts.length >= 3 &&
          desktopPlatforms.contains(parts[1]) &&
          parts.last.endsWith('.node');
      if (!isKeptDesktopNodeFile) continue;
    }
    if (entity is! File) continue;
    final destFile = File('${dest.path}/$relative');
    destFile.parent.createSync(recursive: true);
    entity.copySync(destFile.path);
    bytes += entity.lengthSync();
  }
  return bytes;
}
